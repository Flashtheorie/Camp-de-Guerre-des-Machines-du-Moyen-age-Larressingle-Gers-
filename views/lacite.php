<p class="lead">
	On dit souvent que Larressingle est la "petite Carcassonne" du Gers. Avec raison si l'on veut dire par là que, comme la cité audoise, Larressingle présente au visiteur ses fortifications et son système défensif tels qu'ils étaient au XIIIe siècle. Mais à tort si l'on suggère que Larressingle est une Carcassonne en miniature, un modèle réduit des grandes forteresses médiévales.
</p>


<h3>L'entrée du village :</h3>
<img src="./img/lacite2.jpeg" alt="" class="img-thumbnail">


<p class="lead">La muraille aux douze côtés et aux angles très obtus a retrouvé, depuis sa restauration, sa forme quasi-circulaire, originale par rapport au plan rectangulaire habituel pour les forteresses. Ce tracé savant n'offre aucun point faible, tandis que l'avancement des tours et le surplomb des tours permettent de croiser les tirs sur tous les points menacés. Le système défensif de Larressingle retient l'attention des spécialistes. L'église avec sa haute tour, la salle haute à l'extrados de la voûte qui en font une seconde forteresse au sein de la première ; avec son abside en cul-de-four (percée au XIIIe siècle) et ses chapiteaux historiés, réunit en elle à la fois l'art militaire et l'art religieux.</p>

